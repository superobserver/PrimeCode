def operator_amplitude(n_max, operator):
    a, l, m, p, q = operator
    amplitude = [0] * (n_max + 1)
    for x in range(1, int((n_max / 90)**0.5) + 2):
        n = a * x**2 - l * x + m
        if 0 <= n <= n_max:
            amplitude[n] += 1
            p_x = p + 90 * (x - 1)
            q_x = q + 90 * (x - 1)
            for i in range(1, (n_max - n) // p_x + 1):
                if n + i * p_x <= n_max:
                    amplitude[n + i * p_x] += 1
            for i in range(1, (n_max - n) // q_x + 1):
                if n + i * q_x <= n_max:
                    amplitude[n + i * q_x] += 1
    return amplitude

def mark_composites_k(n_max, operators):
    amplitude = [0] * (n_max + 1)
    for op in operators:
        amp = operator_amplitude(n_max, op)
        for n in range(n_max + 1):
            amplitude[n] += amp[n]
    return amplitude

def compute_amplitudes(n_max, operators_dict, coprime_24):
    amplitudes = {}
    for k in coprime_24:  # Only compute for 24 coprime k
        amplitudes[k] = mark_composites_k(n_max, operators_dict.get(k, []))
    return amplitudes

def compute_zeta_24(args):
    k_values, n_max, s, amplitudes = args
    total_zeta = 0
    for k in k_values:
        k_amplitude = amplitudes[k]
        k_holes = [n for n in range(n_max + 1) if k_amplitude[n] == 0]
        k_primes = [90 * n + k for n in k_holes]
        zeta_k = sum(1 / p**s for p in k_primes if p > 0)
        total_zeta += zeta_k
    return total_zeta

